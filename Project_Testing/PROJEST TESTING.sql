-- Find Highest Retailer --

SELECT TOP 1 R.RETAILER_NAME, SUM(F.SALE_TOTAL) AS HIGHEST_SALES, 
COUNT(F.SALES_ORDER_KEY) AS TOTAL_ORDER FROM TBL_DIM_RETAILER_LKP AS R
LEFT JOIN TBL_FACT_SALES AS F
ON R.RETAILER_KEY = F.RETAILER_KEY
GROUP BY RETAILER_NAME
ORDER BY SUM(F.SALE_TOTAL) DESC, 
COUNT(F.SALES_ORDER_KEY) DESC;

-- Find Lowest Retailer --

SELECT TOP 1 R.RETAILER_NAME, SUM(F.SALE_TOTAL) AS LOWEST_SALES, 
COUNT(F.SALES_ORDER_KEY) AS TOTAL_ORDER FROM TBL_DIM_RETAILER_LKP AS R
LEFT JOIN TBL_FACT_SALES AS F
ON R.RETAILER_KEY = F.RETAILER_KEY
GROUP BY RETAILER_NAME
ORDER BY SUM(F.SALE_TOTAL), COUNT(F.SALES_ORDER_KEY);

-- WAREHOUSE WISE OPEN AND CLOSE INVENTRY --------

SELECT W.ADDRESS AS WAREHOUSE, 
SUM(F.OPENING_INVENTORY) AS OPENING_INVENTORY, 
SUM(F.CLOSING_INVENTORY) AS CLOSING_INVENTORY 
FROM TBL_DIM_WAREHOUSE_LKP AS W
JOIN TBL_FACT_INVENTORY AS F
ON W.WAREHOUSE_KEY = F.WAREHOUSE_KEY
GROUP BY ADDRESS
ORDER BY SUM(F.OPENING_INVENTORY) DESC, 
SUM(F.CLOSING_INVENTORY) DESC;

-- PRODUCT BRAND WISE TOTAL SALES ---

SELECT P.PRODUCT_BRAND,
SUM(F.SALE_TOTAL) AS TOTAL_SALES
FROM TBL_DIM_PRODUCT AS P
LEFT JOIN TBL_FACT_SALES AS F
ON P.PRODUCT_KEY = F.PRODUCT_KEY
GROUP BY PRODUCT_BRAND
ORDER BY PRODUCT_BRAND, SUM(F.SALE_TOTAL) DESC;

-- PRODUCT BRAND WISE TOTAL ORDER ---
SELECT P.PRODUCT_BRAND,
COUNT(F.SALES_ORDER_KEY) AS TOTAL_ORDERS
FROM TBL_DIM_PRODUCT AS P
LEFT JOIN TBL_FACT_SALES AS F
ON P.PRODUCT_KEY = F.PRODUCT_KEY
GROUP BY PRODUCT_BRAND
ORDER BY COUNT(F.SALES_ORDER_KEY) DESC;

