SELECT D.CURRENT_QUARTER, COUNT(SALES_ORDER_KEY) AS TOTAL_ORDER 
FROM TBL_FACT_SALES AS F
LEFT JOIN 
TBL_DIM_DATE_TIME AS D
ON F.SALES_ORDER_KEY = D.DAY_KEY
GROUP BY CURRENT_QUARTER
ORDER BY COUNT(SALES_ORDER_KEY);

-- YEAR WISE TOTAL ORDER

SELECT YEAR(D.DAY_DATE)AS YEAR, COUNT(SALES_ORDER_KEY) AS TOTAL_ORDER 
FROM TBL_FACT_SALES AS F
LEFT JOIN 
TBL_DIM_DATE_TIME AS D
ON F.ORDER_DATE_KEY = D.DAY_KEY
GROUP BY D.DAY_DATE
ORDER BY COUNT(SALES_ORDER_KEY);

