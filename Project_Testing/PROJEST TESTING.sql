-- TOTAL ORDER -- 

SELECT COUNT(SALES_ORDER_KEY) AS TOTAL_ORDER FROM TBL_FACT_SALES;

--- TOTAL ORDER BY PRODUCT ---

SELECT P.PRODUCT_NAME, COUNT(SALES_ORDER_KEY) AS TOTAL_ORDER 
FROM TBL_FACT_SALES AS F
LEFT JOIN TBL_DIM_PRODUCT AS P
ON F.PRODUCT_KEY = P.PRODUCT_KEY
GROUP BY PRODUCT_NAME
ORDER BY COUNT(SALES_ORDER_KEY) DESC;

-- FIND TOTAL ORDERS COUNTRY WISE

SELECT C.COUNTRY_NAME,COUNT(SALES_ORDER_KEY) AS TOTAL_ORDER 
FROM TBL_FACT_SALES AS F
LEFT JOIN TBL_DIM_COUNTRY_LKP AS C
ON F.COUNTRY_KEY = C.COUNTRY_KEY
GROUP BY COUNTRY_NAME
ORDER BY COUNT(SALES_ORDER_KEY) DESC;

