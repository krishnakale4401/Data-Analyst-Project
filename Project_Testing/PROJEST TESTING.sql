-- TOTAL SALES ----

SELECT SUM(SALE_TOTAL) AS TOTAL_SALES FROM TBL_FACT_SALES;

-- TOTAL PROFIT --

SELECT SUM(UNIT_PRICE) - SUM(UNIT_COST) AS TOTAL_PROFIT  FROM TBL_FACT_SALES;

-- TOTAL RETURN --

SELECT COUNT(RETURNED_QUANTITY) AS TOTAL_RETURN FROM TBL_FACT_SALES;

-- TOTAL_PROFIT_PERSENTAGE -- 

SELECT 
SUM(UNIT_PRICE) - SUM(UNIT_COST) -
SUM(SALE_TOTAL) * 100 AS TOTAL_PROFIT_PERSENTAGE
FROM TBL_FACT_SALES;

SELECT 
    ( SUM(UNIT_PRICE) - SUM(UNIT_COST )) * 100.0 / SUM(SALE_TOTAL) AS TOTAL_PROFIT_PERCENTAGE
FROM TBL_FACT_SALES;

-- ORDER METHOD BY TOTAL_SALES ---

SELECT 
    O.METHOD_NAME, 
    SUM(F.SALE_TOTAL) AS TOTAL_SALES
FROM TBL_FACT_SALES AS F
LEFT JOIN TBL_DIM_ORDER_METHOD_LKP AS O
    ON F.ORDER_METHOD_KEY = O.METHOD_KEY
GROUP BY METHOD_NAME
ORDER BY SUM(F.SALE_TOTAL) DESC;

-- ORDER METHOD BY TOTAL_ORDER ---

SELECT O.METHOD_NAME, COUNT(SALES_ORDER_KEY) AS TOTAL_ORDER 
FROM TBL_FACT_SALES AS F
LEFT JOIN TBL_DIM_ORDER_METHOD_LKP AS O
ON F.ORDER_METHOD_KEY = O.METHOD_KEY
GROUP BY METHOD_NAME
ORDER BY COUNT(SALES_ORDER_KEY) DESC;

-- FIND TOP 10 PRODUCT SALES WISE --
SELECT TOP 10 P.PRODUCT_NAME, SUM(F.SALE_TOTAL) AS HIGHEST_SALES FROM TBL_DIM_PRODUCT AS P
LEFT JOIN TBL_FACT_SALES AS F
ON P.PRODUCT_KEY = F.PRODUCT_KEY
GROUP BY PRODUCT_NAME
ORDER BY SUM(F.SALE_TOTAL) DESC;

-- FIND LOWEST 10 PRODUCT SALES WISE --
SELECT TOP 10 P.PRODUCT_NAME, SUM(F.SALE_TOTAL) AS HIGHEST_SALES FROM TBL_DIM_PRODUCT AS P
LEFT JOIN TBL_FACT_SALES AS F
ON P.PRODUCT_KEY = F.PRODUCT_KEY
GROUP BY PRODUCT_NAME
ORDER BY SUM(F.SALE_TOTAL);

