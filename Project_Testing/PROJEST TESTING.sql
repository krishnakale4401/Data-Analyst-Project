--- FIND PRODUCT NAME WITH HIGHEST SALES ---

SELECT TOP 1 P.PRODUCT_NAME, SUM(F.SALE_TOTAL) AS HIGHEST_SALES 
FROM TBL_DIM_PRODUCT AS P
LEFT JOIN TBL_FACT_SALES AS F
ON P.PRODUCT_KEY = F.PRODUCT_KEY
GROUP BY PRODUCT_NAME
ORDER BY SUM(F.SALE_TOTAL) DESC;

--- FIND PRODUCT NAME WITH LOWEST SALES ---

SELECT TOP 1 P.PRODUCT_NAME, SUM(F.SALE_TOTAL) AS HIGHEST_SALES 
FROM TBL_DIM_PRODUCT AS P
LEFT JOIN TBL_FACT_SALES AS F
ON P.PRODUCT_KEY = F.PRODUCT_KEY
GROUP BY PRODUCT_NAME
ORDER BY SUM(F.SALE_TOTAL);

--- FIND OPEN AND CLOSE INVENTRY PRODUCT WISE ---

SELECT P.PRODUCT_NAME, 
SUM(F.OPENING_INVENTORY) AS OPENING_INVENTORY, 
SUM(F.CLOSING_INVENTORY) AS CLOSING_INVENTORY 
FROM TBL_DIM_PRODUCT AS P
LEFT JOIN TBL_FACT_INVENTORY AS F
ON P.PRODUCT_KEY = F.PRODUCT_KEY
GROUP BY PRODUCT_NAME
ORDER BY SUM(F.OPENING_INVENTORY) DESC, 
SUM(F.CLOSING_INVENTORY) DESC;

-- FIND TOTAL SALE BY CITY ---

SELECT W.CITY, SUM(F.SALE_TOTAL) AS HIGHEST_SALES 
FROM TBL_DIM_WAREHOUSE_LKP AS W
JOIN TBL_FACT_SALES AS F
ON W.BRANCH_CODE = F.COUNTRY_KEY
-- WHERE CITY = 'München'  
GROUP BY CITY
ORDER BY SUM(F.SALE_TOTAL)DESC;

